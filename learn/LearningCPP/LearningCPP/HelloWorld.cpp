#include <iostream>
using namespace std;

int main() {
	// With the using namespace approach
	cout << "Hello World!";

	// Directly referencing the library
	std::cout << "Hello World with direct reference!";


	// Line breaks:
	cout << "Line \n Break";

	// Another option:
	cout << "Line " << endl << " Break";

	// Program executed successfully
	return 0;
}

